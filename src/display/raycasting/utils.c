bool	is_voided_door(char c)
{
	if (c == 'q' || c == 'w' || c == 'e' || c == 'r' || c == 'Q' || c == 'W'
		|| c == 'E' || c == 'R' || c == 'O')
		return (true);
	return (false);
}
